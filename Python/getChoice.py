		 if x >= startX - 2 and x <= endX + 2:
		     out = 'B'
		     break
		 startX = endX + 10
		 endX = startX + 40
		 if x >= startX - 2 and x <= endX + 2:
		     out = 'C'
		     break
		 startX = endX + 10
		 endX = startX + 40
		 if x >= startX - 2 and x <= endX + 2:
		     out = 'D'
		     break
		 startX = endX + 10
		 endX = startX + 40
